package br.com.i9.conhecimento.tipofloat;

/**
 * O float é um tipo primitivo numérico de range -1,4024E-37 até 3,40282347E+38.
 * Ele é um dos tipos primitivos usados para trabalhar com números de ponto
 * flutuante, servindo para garantir precisão nos cálculos e para notação
 * científica. Bora descobrir um pouco mais?                */
public class Float {

	public static void main(String[] args) {
		float numA = -101.23f;
		float numB = 2.356f;
		System.out.println(numA + numB); // -98.874
	}

}
